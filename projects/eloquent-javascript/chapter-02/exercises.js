function triangles(levels) {
  let arr = [];
  for(let i = 0; i < levels; i++){
    arr[i] = '#';
  }
  for(let i = 0; i < levels; i++){
    for(let j = 0; j < i; j++){
      arr[i] += '#';
    }
  }
  for(let i = 0; i < levels; i++){
    console.log(arr[i]);
  }
}

